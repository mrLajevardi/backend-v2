import { Controller, Get, Post, Put, Delete, Body, Param } from '@nestjs/common';
import { ApiTags, ApiOperation, ApiResponse, ApiBearerAuth } from '@nestjs/swagger';
import { MigrationsLock } from 'src/infrastructure/database/entities/MigrationsLock';
import { MigrationsLockService } from './migrations-lock.service';
import { CreateMigrationsLockDto } from 'src/infrastructure/dto/create/create-migrations-lock.dto';
import { UpdateMigrationsLockDto } from 'src/infrastructure/dto/update/update-migrations-lock.dto';


@ApiTags('MigrationsLock')
@Controller('migrations-lock')
@ApiBearerAuth() // Requires authentication with a JWT token
export class MigrationsLockController {
  constructor(private readonly service: MigrationsLockService) {}

  // Find an item by id 
  @ApiOperation({ summary: 'Find an item by ID' })
  @ApiResponse({ status: 200, description: 'Return the found item' })
  @Get(':id')
  async findById(@Param('id') id: string): Promise<MigrationsLock> {
    return this.service.findById(id);
  }

  // find items using search criteria 
  @ApiOperation({ summary: 'Find items using search criteria' })
  @ApiResponse({ status: 200, description: 'Return the found items' })
  @Get()
  async findAll(): Promise<MigrationsLock[]> {
    return this.service.find({});
  }

  // create new item 
  @ApiOperation({ summary: 'Create a new item' })
  @ApiResponse({ status: 201, description: 'The item has been successfully created' })
  @Post()
  async create(@Body() dto: CreateMigrationsLockDto): Promise<void> {
    await this.service.create(dto);
  }

  // update an existing item 
  @ApiOperation({ summary: 'Update an existing item' })
  @ApiResponse({ status: 200, description: 'The item has been successfully updated' })
  @Put(':id')
  async update(@Param('id') id: string, @Body() dto: UpdateMigrationsLockDto): Promise<void> {
    await this.service.update(id, dto);
  }


  //delete an item
  @ApiOperation({ summary: 'Delete an item' })
  @ApiResponse({ status: 200, description: 'The item has been successfully deleted' })
  @Delete(':id')
  async delete(@Param('id') id: string): Promise<void> {
    await this.service.delete(id);
  }
}
